package prog2.model;
public class CentralUBException extends Exception {


    public CentralUBException() {
        super();
    }

    public CentralUBException(String message) {
        super(message);
    }

    public CentralUBException(String message, Throwable cause) {
        super(message, cause);
    }

    public CentralUBException(Throwable cause) {
        super(cause);
    }
}
